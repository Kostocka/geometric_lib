
def area(a):
    '''
    Take the length side and return the area
    :To calculate the area of a square, it is enough to know one side
    :print(area(4))  # Output: 16
    '''
    return a * a


def perimeter(a):
    '''
    Take the length side and return the perimeter
    :print(perimeter(4))  # Output: 16
    '''
    return 4 * a
