
def area(a, b): 
    '''
    Take the lengths of the two sides and return the area
    :print(area(4, 5))  # Output: 20'
    '''
    return a * b

def perimeter(a, b): 
    '''
    Take the lengths of the two sides and return perimeter using the formula 2a + 2b
    : print(perimeter(4, 5))  # Output: 18
    '''
    return (a + b ) * 2